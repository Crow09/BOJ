#include <bits/stdc++.h>
using namespace std;

int main() {
	int t;
	cin >> t;
	for (int r = 1; r <= t; r++) {
		int n, cnt = 0;
		scanf("%d", &n);
		while (n >= 5) {
			n/=5;
			cnt+=n;
		}
		printf("%d\n", cnt);
	}
	return 0;
}
